            document.querySelectorAll('.upgrade-toggle').forEach(function (button) {
                button.addEventListener('click', function () {
                    var expanded = button.getAttribute('aria-expanded') === 'true';
                    var details = button.parentElement.querySelector('.upgrade-details');
                    var icon = button.querySelector('.upgrade-icon');

                    button.setAttribute('aria-expanded', expanded ? 'false' : 'true');
                    if (expanded) {
                        details.style.maxHeight = '0px';
                        details.classList.remove('opacity-100', 'mt-2');
                        details.classList.add('opacity-0', 'mt-0');
                    } else {
                        details.style.maxHeight = details.scrollHeight + 'px';
                        details.classList.remove('opacity-0', 'mt-0');
                        details.classList.add('opacity-100', 'mt-2');
                    }
                    icon.style.transform = expanded ? 'rotate(0deg)' : 'rotate(225deg)';
                });
            });
